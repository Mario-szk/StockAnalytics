package com.stockanalytics.bean;

public class Market {
	private ShangZheng shanghai;
	private ShenZheng shenzhen;
	
	public ShangZheng getShanghai() {
		return shanghai;
	}
	public void setShanghai(ShangZheng shanghai) {
		this.shanghai = shanghai;
	}
	public ShenZheng getShenzhen() {
		return shenzhen;
	}
	public void setShenzhen(ShenZheng shenzhen) {
		this.shenzhen = shenzhen;
	}
	@Override
	public String toString() {
		return "Market [shanghai=" + shanghai + ", shenzhen=" + shenzhen + "]";
	}
	
	
}
